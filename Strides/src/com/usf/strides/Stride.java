package com.usf.strides;

public class Stride {	
	public Stride() {
		super();
	}
	
	public String getStride() {
		return Stride;
	}
	
	public String getTerm() {
		return Term;
	}
	
	public void setTerm(String t) {
		this.Term = t;
	}
	
	public void setStride(String s) {
		this.Stride = s;
	}
	
	private String Stride;
	private String Term;
}
